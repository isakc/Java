package test;

public class Hello {
	
	//자바에서 실행하면 무조건 이 main
	//메소드가 작동됩니다.
	public static void main(String[] args){
		
		//콘솔에 출력
		System.out.println("Hello World!");
		
	}
	
	/*
	 * 자바는 클래스 기반
	 * 
	 * 우리가 작성한 파일 : .java
	 * (2byte, 유니코드 : 인간이 볼 수
	 * 있는 파일)
	 * 
	 * 실행을 하면 컴파일러가
	 * 컴파일을 해서 .class파일을 생성
	 * (바이너리파일, 1byte, 반기계어)
	 * 
	 * 자바가상머신에서 .class파일이
	 * 작동되서 실행됨
	 * 
	 * 이클립스에서 Ctrl + F11을 눌러서
	 * 실행하면 컴파일러를 작동시켜서
	 * .class파일을 bin폴더에 만들고
	 * 그 .class파일을 실행시킴
	 * 
	 */
}