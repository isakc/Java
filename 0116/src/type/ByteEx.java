package type;

public class ByteEx {
	public static void main(String args[]){
		
		/*
		 * 자바의 정수형
		 * 
		 * 1) byte : 1byte(8bit) : -128 ~ 127
		 * 
		 * 2) short : 2byte : -32,768 ~ 32,767
		 * 
		 * 3) int : 4byte(자바의 기본정수)
		 * 
		 * 4) long : 8byte
		 * 
		 * 
		 */
		
		//byte형 자료를 담을 수 있는 변수
		//b를 선언
		byte b;

		//기본자료형 byte형 변수 b에
		//-128을 대입
		b = -128;
		
		
		System.out.println(b);
		

	}// main() end
}// ByteEx class end