package type;

public class LongEx {
	public static void main(String[] args) {
		
		/*
		 * long형 8byte
		 * 정수의 기본값 : 0
		 * 
		 * long형은 제일 뒤에 L(l)을 써준다
		 */
		
		//기본자료형 long형 변수 l을 선언하고
		//정수 2147473648을 대입(초기화)
		long l = 2147473648L;
		
		System.out.println(l);

	}//main() end
}// LongEx class end