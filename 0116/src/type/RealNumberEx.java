package type;

public class RealNumberEx {
	public static void main(String args[]){
		
		/*
		 * 자바의 실수
		 * 
		 * 1)float : 4byte
		 * 
		 * 2)double : 8byte (기본실수)
		 * 
		 */
		
		//기본자료형 double 변수 d를 선언하고
		//실수 3.12을 대입 (초기화)
		double d = 3.12;
		
		//기본자료형 float 변수 f를 선언하고
		//실수 3.12을 대입 (초기화)
		float f = 3.12f;
		
		System.out.println(f);
	}
}