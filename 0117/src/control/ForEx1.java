package control;

public class ForEx1 {
	public static void main(String[] args) {
	
		/*
		 * 제어문중에 반복문
		 * 
		 * 1) for문
		 * 2) while문
		 * 
		 * -for문의 문법
		 * 
		 * for(초기식; 조건식; 증감식){
		 *    반복구문
		 * }
		 */
		
		for(int i = 1; i <= 10 ; i++){
			System.out.println(i);
		}//for end
		
	}//main() end
}//ForEx1 class end