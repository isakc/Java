package control;

public class ForEx4 {
	public static void main(String[] args) {

		for (int i = 0; i < 2; i++) {
			for (int j = 0; j < 3; j++) {
				System.out.println(i + " : " + j );
			} // 안쪽 for end
		} // 바깥쪽 for end

		
		System.out.println("****************************");
		
		System.out.print("123");
		System.out.print("\t");
		System.out.print("456");
		
		
	}// main() end
}// ForEx4 class end