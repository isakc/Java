package operator;

public class OperatorEx3 {
	public static void main(String[] args) {
	
		/*
		 * 삼항연산자 (if ~ else 변경)
		 * 
		 * 피연산자1 ? 피연산자 : 피연산자3
		 * 
		 * - 피연산자1 : 조건식(논리값)
		 * - 피연산자2, 3은 같은 자료형
		 *  
		 */
		
		int z = true ? 3 : 6;
		System.out.println(z);
		
		
	}//main() end
}//OperatorEx3 class end