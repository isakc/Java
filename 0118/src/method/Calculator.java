package method;

public class Calculator {
	// 덧셈연산 메소드
	// 덧셈을 하고 그 결과를 리턴하는
	// 메소드
	public int sum(int x, int y) {
		return x + y;
	}// sum() end

	public int subtract(int x, int y) {
		return x - y;
	}// subtract() end

	public int multiply(int x, int y) {
		return x * y;
	}// multiply() end

	public double divide(int x, int y) {
		return (double)x / y;
	}// divide() end

}// Calculator class end
